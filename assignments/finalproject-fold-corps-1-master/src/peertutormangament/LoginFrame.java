    private void adminLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminLoginButtonActionPerformed
        // init variables
        loginPanel1.adPw = pwAdminLoginField.getText();
        loginPanel1.adUser = userAdminLoginField.getText();
        
        if (loginPanel1.isAdmin()) {
            // navigate to admin welcome page, close login
            adminSearchFrame = new AdminSearchFrame();
            setVisible(false);
            adminSearchFrame.setVisible(true);
        } else {
            // display error message
            String msg = "Incorrect credentials";
            JOptionPane.showMessageDialog(null, msg);
            // clearing fields
            userAdminLoginField.setText(null);
            pwAdminLoginField.setText(null);
        }
    }//GEN-LAST:event_adminLoginButtonActionPerformed


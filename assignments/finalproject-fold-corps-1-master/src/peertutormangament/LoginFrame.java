    private void studentLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentLoginButtonActionPerformed
        // TODO add your handling code here:
        // initializing variables
        loginPanel1.givenUser = userStudentLoginField.getText();
        loginPanel1.givenPw = pwStudentLoginField.getText();
        try {
            // reading the file before comparison
            loginPanel1.read();
        } catch (FileNotFoundException ex) {
            // error message
            String msg = "Error - needed files are missing";
            JOptionPane.showMessageDialog(null, msg);
        }
        if (loginPanel1.exist() == -1) {
            // display error message - this user does not exist
            String msg = "This user does not exist";
            JOptionPane.showMessageDialog(null, msg);
            // clearing text fields
            userStudentLoginField.setText(null);
            pwStudentLoginField.setText(null);
        } else if (loginPanel1.valid() == false) {
            // display error message - incorrect password for given username
            String msg = "Incorrect password for the given username";
            JOptionPane.showMessageDialog(null, msg);
            // clearing pw field
            pwStudentLoginField.setText(null);
        } else {
            tutee = loginPanel1.tutees.get(loginPanel1.exist());
            // play login sound effect
            soundEffect("login.wav");
            try {
                // navigate to student welcome page, close login
                studentWelcomeFrame = new StudentWelcomeFrame();
                studentWelcomeFrame.welcomeLabel.setText("Welcome, " + tutee.fullName + "!"); 
            } catch (FileNotFoundException ex) {
                Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            setVisible(false);
            studentWelcomeFrame.setVisible(true);
        }
    }//GEN-LAST:event_studentLoginButtonActionPerformed
    /**
     * Plays sound effect for login buttons
     * @param s - audio file name
     */
    public void soundEffect (String s) {
        InputStream sound;
        try {
            //finds the file with a name matching String s
            sound = new FileInputStream(new File(s));
            //intializes audiostream object to play audio
            AudioStream audio = new AudioStream(sound);
            //plays the audio
            AudioPlayer.player.start(audio);
        } catch (IOException e) {
            String msg = "A necessary file is missing.";
            JOptionPane.showMessageDialog(null, msg);
        }
    }
